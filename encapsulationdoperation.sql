--********************************************************************************************
-- Enregistrement d'une nouvelle personne étudiante
--********************************************************************************************
CREATE OR REPLACE  PROCEDURE process_nouveau_etudiant (
    PnumTelephone        varchar(30)    ,
    PNom                 varchar(255)   ,
    PPrenom              varchar(255)   ,
    PnumAdressSocial     varchar(255)   ,
    PCodePermanent       VARCHAR(255)   ,
    PAdresseCivique      VARCHAR(255)   ,
    PCourriel            VARCHAR(255)   ,
    PCourrielUqac        VARCHAR(255)   ,
    Petat                CHAR
)
    AS
   BEGIN
      INSERT INTO Personne
          ( numTelephone,
            Nom,
            Prenom  ,
            numAdressSocial
            )
       VALUES
        (PnumTelephone,
        PNom   ,
        PPrenom  ,
        PnumAdressSocial
        );
      INSERT INTO Etudiant
          (
            numTelephone,
            CodePermanent ,
            AdresseCivique   ,
            Courriel     ,
            CourrielUqac  ,
            etat
    )
       VALUES
        (PnumTelephone,
        PCodePermanent ,
        PAdresseCivique,
        PCourriel,
        PCourrielUqac,
        Petat);
   END;
